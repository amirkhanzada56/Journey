# Tuples in python

# Creating
my_tuple = ("apple", "banana", "orange", "apple")

# Access Elements
print(my_tuple[1])        # banana
print(my_tuple[-1])       # orange

# Modify
# fruits[1] = "orange" (error - tuple immutable)

# Methods
print(my_tuple.count("apple"))      # 2
print(my_tuple.index("apple"))      # 0

