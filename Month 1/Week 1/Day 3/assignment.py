# Assignment Operators

a = 5

a += 3
print(a)    # a = a + 3 --> 8

a -= 3
print(a)    # a = a - 3 --> 5

a *= 3
print(a)    # a = a * 3 --> 15

a /= 3
print(a)    # a = a / 3 --> 5.0

a //= 3
print(a)    # a = a // 3 --> 1.0

a %= 3
print(a)    # a = a % 3 --> 1.0

a **= 3
print(a)    # a = a ** 3 --> 1.0