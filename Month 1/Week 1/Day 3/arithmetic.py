# Arithmetic Operators

a = 3
b = 2

print("Addition :", a + b)          # 5
print("Subtraction :", a - b)       # 1
print("Multiplication :", a * b)    # 6
print("Division :", a / b)          # 1.5
print("Floor Division :", a // b)   # 1
print("Modulus: ", a % b)           # 1
print("Power: ", a ** b)            # 9